    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        String id = jTextField2.getText();
       
        
        try {
            pst = con.prepareStatement("select * from doktor where id=?");
            pst.setString(1, id);
            rs = pst.executeQuery();
            
            if(rs.next()){
                String ad = rs.getString("doktor_isim");
                String soyad = rs.getString("doktor_soyisim");
                String poli = rs.getString("poliklinik");
                String ünvan = rs.getString("doktor_ünvan");
                
                
                Arama obj4 = new Arama(ad,soyad,poli,id,ünvan);
                obj4.setVisible(true);
                
                
            }else{
                JOptionPane.showMessageDialog(this,"Personel No Bulunamadı");
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(GirisSayfası.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

