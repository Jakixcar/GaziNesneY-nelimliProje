    public void HastaTable(){
        
        try {
            pst = con.prepareStatement("select * from hasta");
            rs = pst.executeQuery();
            ResultSetMetaData Rsm = rs.getMetaData();
            int c;
            c = Rsm.getColumnCount();
            
            DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            
            while(rs.next())
            {
                Vector v2 = new Vector();
                
                for(int i  = 1; i<= c ;i++){
                v2.add(rs.getString("tc"));
                v2.add(rs.getString("ad"));
                v2.add(rs.getString("soyad"));
                v2.add(rs.getString("cinsi"));
                v2.add(rs.getString("memleket"));
                v2.add(rs.getString("baba"));
                v2.add(rs.getString("ana"));
                v2.add(rs.getString("parola"));
                }
                df.addRow(v2);
            }
                
        } catch (SQLException ex) {
            Logger.getLogger(DanışmanEkranı.class.getName()).log(Level.SEVERE, null, ex);
        }           
    }
    
